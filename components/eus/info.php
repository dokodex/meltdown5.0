<strong>MELTDOWN</strong> 2013an Hernanin, Euskal Herrian, sortutako <strong>Hardcore Metal</strong> talde bat da.<br><br>

      <strong>Lehenengo urteak</strong> (2013-2015)<br><br>


      <strong>2014ko uztailean</strong> beraien <strong>lehen EPa</strong> aurkezten dute, Meltdown izena izango dueña Alvaro Remachak grabatua (Materia, Cohen, LionHead, All Became Dust…). EP homonimo honek kritika onak izango ditu Rockzone edo El Diario Vasco bezalako komunikabideetan. Honen ondorioz Euskal Herri osotik Dawn of the Maya, Bellako, Adrenalized edo Hyde Abbey bezalako taldeekin kontzertuak jotzeko aukerak izango dituzte.<br><br>

      <strong>2015an</strong>, kontzertuz lepo izandako hilabete batzuen ondoren, beraien bigarren EPa izango dena konposatzeko tartea hartzea erabakitzen dute. Denbora baten ondoren, oholtzen gainera itzultzen dira eta hainbat konkurso irabazi ondoren beraien <strong>single berria aurkezten dute “Oxymoron”</strong>, beraien lehen lanaren eta bigarrenaren arteko trantsizio bezala balioko diena.<br><br>


      <strong>I Refuse to Die Here</strong> (2016-2017)<br><br>


      <strong>2016 urtearen hasieran “I Refuse to Die Here”</strong> lana argitaratzen dute, beraien bigarren EPa <strong>Alex Capparen</strong> eskutik “The Metal Factory Studios”en, Madrilen (Vita Imana, Hamlet, Dawn of the Maya…), zeinak <strong>peninsulara burutzeko</strong> lagunduko dien non Madril, Bartzelona edo Balentzia bezalako hiriburuetan jotzeko aukera duten. Hare gehiago, Crisix, In Mute, Killus edo Dawn of the Maya bezalako taldeekin jotzeko aukera izango dute.<br><br>

       Udaran atsedenaldi txiki baten odnoren, <strong>penintsulan kontzertu berriak</strong> eskaintzen hasiko dira berriro, horaingoan <strong>baxujole berriarekin</strong>, Pablo Ferreras, zeinak bostgarren taldekide bezala jardungo duen.<br><br>



      2016ko azaroan, Meltdown <strong>Atlas Artist Agency</strong>-ra bilduko dira, Rove Hernandok eta Javi Carrionek (<strong>Crisix</strong> thrash talde kataluinarraren tour managerra eta baterijolea) sortutako management eta booking agentzia bat. 2016ko abenduan, Meltdownek <strong>lehen Europar gira egingo dute</strong> non 9 egunez Franztia, Suiza eta Italia pasako dituzten.<br><br>


      <strong>2017ko urtarrilean</strong>, <strong>beste estatu bira</strong> batetan murgilduko dira <strong>Dawn of the Maya</strong> metalcore talde azpimarragarriaren “Colossal” lanaren aurkezpenean, beraien hirugarren LPa. Gira honek, <strong>core eszenako talde aktiboenetako bat</strong> bilakatuko du Meltdown, zeinek urtea giratzen eta konposatzen pasako duen.<br><br>



      <strong>From This Day To The Grave</strong> (2018)<br><br>

      2018ko urtarrilan Meltdownek <strong>"From This Day To The Grave"</strong> diska berriaren argitalpena adierazten du, <strong>Art Gates Records</strong> (ESP/POR) eta <strong>Imminence Records</strong> (USA) disketxeekin egindako akordioekin batera.

      Lan berri honek taldearen historiako lorpen handienak ekartzen ditu: gelditu gabe egoten dira biran <strong>Europa osoan zehar, estatutik kanpo 25 kontzertu</strong> baina gehiago ematen Alemania, Suitza, Frantzia, Txekiar Errepublika eta Polonia bezalako herrialdeetan. <strong>Download Festival Madrid</strong> eta Out of Sight Festival (POR) bezalako festibal handietan ere parte hartzen dute, "New Metal Tracks" bezalako Spotifyko playlist handietan agertzen dira eta musika berria argitaratzen dute, Linkin Park talde esanguratsuaren One Step Closer abestia moldatuz "HCXHC Vol. 3" izeneko abesti bilduma solidariorako, kantzerraren biktimei zuzendutako proiektu bat.

      Urte amaieran, Meltdownek <strong>Resurrection Fest 2019</strong> festibalean emanaldi bat emango dutela adierazten du, estatu mailako metal festibal handienean. Munduaren dominazioa da hurrengo pausoa. Hau jada betirako da.
